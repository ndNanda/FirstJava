public class MultiplicationTable {

    public static void main(String[] args) 
{

        int num1 = 13,num2 = 19;
        for(int i = 1; i <= 10; ++i)
        {
            
           System.out.println(num1 + " * " + i + " = " + num1 * i);


           System.out.println(num2 + " * " + i + " = " + num2 * i);
        }
    }
}